package com.learning.rabbitmq.demo.entity.mybatis;

import javax.persistence.*;

public class Role {
    @Id
    private String id;

    private String role;

    /**
     * @return id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role
     */
    public void setRole(String role) {
        this.role = role;
    }
}